                                <div class="member-section">
                                    <div class="member-content">
                                        <p>Authors are advised to save/submit their research video under the <b>.mp4</b>
                                            file extension. <br>Note that many open-source software can be used to convert
                                            your video from multiple file formats to mp4 file format, such as <a
                                                href="https://www.openshot.org/download/">openshot</a>: <br><br>
                                            <b>⬛ 2D Videos:</b> All <b>2D</b> videos must have a resolution that is lower
                                            than or equal to (1024 x 600) pixels. <b>2D</b> Videos generated at 60 FPS
                                            (Frames Per Second) are recommended.<br><br>
                                            <b>⬛ 360° and VR Videos:</b> <br><br>
                                            <b>• 360° or Monoscopic Videos:</b> The most common type, <b>360°</b> monoscopic
                                            video, consists of an image as an equirectangular projection (as a world map
                                            spherical in shape) that is viewed as a flat image. <br>Monoscopy typically has
                                            an <b>aspect ratio</b> of <b>2:1</b>, and common <b>monoscopic video
                                                resolutions</b>: <b>(840 x 1920), (4096 x 2048), (5760 x 2880), or (7680 x
                                                3840) pixels</b>.<br>
                                            <b>360°</b> Videos generated at <b>60 FPS</b> (Frames Per Second) are
                                            recommended.<br><br>
                                            <b>• <b>VR</b> (Virtual Reality) or Stereoscopic Videos:</b> This type of
                                            <b>VR</b> video uses two images for the eyes (one image per eye) that are
                                            projected <b>SBS</b> (<b>Side/By/Side 3D</b>) or (<b>Top/Bottom 3D</b>) with
                                            different wide-view angles, with slightly different perspectives, to create a
                                            <b>3D</b> effect when used with <b>VR</b> headsets or <b>VR</b> eyeglasses and
                                            other viewing hardware. Stereoscopic immersive video can has an <b>aspect
                                                ratio</b> of <b>1:1</b>, with these common <b>stereoscopic resolutions</b>:
                                            <b>(3840 x 3840), (5120 x 5120) or (7680 x 7680) pixels</b>.<br><br>
                                            Kindly note that Anaglyph <b>3D</b>, commonly seen in Red/Cyan, is an old school
                                            fashion to watch <b>3D</b> content, and thus they will not be accepted for
                                            online upload to <a href="https://researchvideos.net">ResearchVideos.net</a>
                                            <br><br>
                                            The following <b>mp4 file sizes</b> are <b>required upon uploading</b> a new
                                            video to <a href="https://researchvideos.net">ResearchVideos.net</a>:<br><br>
                                            <b>• 2D Research Video:</b> Maximum Size Allowed = <b>500 MB</b><br>
                                            <b>• 360° or VR Research Video:</b> Maximum Size Allowed = <b>5 GB</b><br><br>
                                            <b>Note</b>: Authors may adjust the resolution, frequency, or the total time
                                            period of their <b>mp4</b> video.
