                                <h5 align="center">12- Peer Review Process:</h5>
                                <div class="member-section">
                                    <div class="member-content">
                                        <p>Upon submission of a new video to <a
                                                href="https://researchvideos.net">ResearchVideos.net</a>, it will be
                                            allocated to an Editorial Member based on the video's scientific discipline and
                                            subdiscipline. This assigned Editorial Member, possessing expertise in the
                                            relevant discipline, will conduct a thorough review of the submitted
                                            video.<br><br>
                                            The <i>Editorial Member</i> will expediently assess the video or delegate the
                                            task to two or more reviewers as part of the one-way blinded-review process. The
                                            assigned reviewers, having accepted the review task, will carefully evaluate the
                                            video contents, and provide their feedback in the form of recommendations (i.e.
                                            <i>accept, reject, or revise</i>) to the assigned Editorial Member.<br><br>
                                            Subsequently, the <i>Editorial Member</i> will consolidate the review results
                                            and make a final decision, which will be communicated to the corresponding
                                            author via email. This robust review process ensures the quality and integrity
                                            of content on our platform. We will try to complete the review process of a
                                            submitted in an averaged period of time of about one to two months maximum.</p>
