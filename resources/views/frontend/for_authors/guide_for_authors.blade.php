                                <div class="member-section">
                                    <div class="member-content">
                                        <p>
                                        <p><a href="https://researchvideos.net">ResearchVideos<span class="tmFontSize">&trade;</span></a> will consider the
                                            following types of video submissions:<br><br>
                                            <b>• Video Clips:</b> Less than or equal to 10 minutes video based on a paper previously published
                                            by the author in a conference or peer-reviewed journal. Authors are required to
                                            be mindful of copyright regulations when incorporating data from their existing
                                            publication.<br><br> Generally, papers published under a CC-BY license are
                                            suitable for sharing in this format. For more details about this video type
                                            refer to the following section in this guideline.<br><br>
                                            <b>• Research Video:</b> Less than or equal to 15 minutes video that presents the findings of a
                                            new study. It typically follows a structured format, including segments such as
                                            an introduction, brief literature review, methodology, results, discussion, and
                                            conclusion.<br><br>
                                            <b>• Review Video:</b> Less than or equal to 25 minutes video by distinguished experts presenting
                                            a comprehensive synthesis of existing scientific literature on a specific topic.
                                            This type of video aims to offer a balanced mixture of established scientific
                                            knowledge, coupled with insightful discussions and perspectives on the subject
                                            matter.<br><br>
                                            <b>• Short Video:</b> Less than or equal to 5 minutes concise video designed for the rapid
                                            dissemination of novel research content. This video type may spotlight a
                                            specific facet of a problem or unveil a novel finding anticipated to make a
                                            substantial impact.<br><br> Examples of content suitable for Short Videos
                                            involve the discovery or development of new ideas, pioneering experiments and
                                            theory, and innovation in simulation and modeling. Given their emphasis on
                                            delivering a more pronounced impact rather than incremental research reporting,
                                            Short Videos benefit from prioritized and expeditious publication
                                            processes.<br><br>
                                            <b>• Tutorial Video:</b> Less than or equal to 20 minutes instructional video providing
                                            step-by-step guidance on diverse subjects, such as software usage, experimental
                                            methods, calculation approaches, and manufacturing processes. These videos aim
                                            to offer clear, user-friendly explanations to facilitate a better understanding
                                            of complex concepts or procedures.<br><br>
                                            <b>• News and Updates:</b> Less than or equal to 2 minutes, informative video delivering the
                                            latest developments and noteworthy information within a specific domain. Whether
                                            highlighting recent advancements, conference announcements, industry trends, or
                                            key broadcasts, these videos serve as dynamic updates to keep the audience
                                            alongside current events.
